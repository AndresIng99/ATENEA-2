function activateblue() {
    document.getElementById('moto_azul').style.display="block";
    document.getElementById('moto_gris').style.display="none";
    document.getElementById('moto_negra').style.display="none";
}
function activategray() {
    document.getElementById('moto_azul').style.display="none";
    document.getElementById('moto_gris').style.display="block";
    document.getElementById('moto_negra').style.display="none";
}
function activateblack() {
    document.getElementById('moto_azul').style.display="none";
    document.getElementById('moto_gris').style.display="none";
    document.getElementById('moto_negra').style.display="block";
}